    public function setNom(string $nom): static
    {
        $this->nom = $nom;

        return $this;
    }

    public function getAddPostal(): ?string
    {
        return $this->add_postal;
    }

    public function setAddPostal(string $add_postal): static
    {
        $this->add_postal = $add_postal;

        return $this;
    }

