    /**
     * @return Collection<int, Bid>
     */
    public function getBid(): Collection
    {
        return $this->bid;
    }

    public function addBid(Bid $bid): static
    {
        if (!$this->bid->contains($bid)) {
            $this->bid->add($bid);
            $bid->setUser($this);
        }

        return $this;
    }

    public function removeBid(Bid $bid): static
    {
        if ($this->bid->removeElement($bid)) {
            // set the owning side to null (unless already changed)
            if ($bid->getUser() === $this) {
                $bid->setUser(null);
            }
        }

        return $this;
    }
    public function __toString(): string
    {
        return $this->getNom() ?: 'User-' . $this->getId();
